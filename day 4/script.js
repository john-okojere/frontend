$('#create').hide()
$('#favourite').hide()

function create() { 
    $('#create').show()
    $('#find').hide()
    $('#favourite').hide()
            
}
function find() { 
    $('#create').hide()
    $('#find').show()
    $('#favourite').hide()
            
}
function favourite() { 
    $('#create').hide()
    $('#find').hide()
    $('#favourite').show()
            
}