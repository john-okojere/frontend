
alert(`The prime numbers between 100 and 9999 are:`);

// looping from lowerNumber to 9999
for (let i = 100; i <= 9999; i++) {
    let flag = 0;

    // looping through 2 to user input number
    for (let j = 2; j < i; j++) {
        if (i % j == 0) {
            flag = 1;
            break;
        }
    }

    // if number greater than 1 and not divisible by other numbers
    if (i > 1 && flag == 0) {
        console.log(i);
    }
}